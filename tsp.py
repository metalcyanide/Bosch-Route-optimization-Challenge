def dpcost(currset, end, weightmatrix):
    currset = tuple(currset)
    if (currset,end) in dp.keys():
        return dp[currset,end]
        
    setnew = copy.deepcopy(list(currset))
    setnew.remove(end)
    setnew = tuple(setnew)
    minlist = []
    for j in currset:
        if j != end and j!=start:
            minlist.append(dpcost(setnew,j,weightmatrix) + weightmatrix[j][end])
    
    minimum = min(minlist)

    dp[currset,end] = minimum

    return dp[currset,end]

def createdp(currset,start = start):
    for i in currset:
        if i != start:
            dpcost(currset,i, weightmatrix)
    
'''
For a given weightmatrix with given set of points to be present in the path
this function returns the shortest path starting at 'start' point and 
covering all points of 'currset'
'''
def findpath(weightmatrix, dp, currset, path = [], start= start):
    currsetstart = tuple(currset)
    currset.remove(start)
    prevval = start
    path.append(start)
    while(len(currset) > 1):
        
        minimum = dp[currsetstart,currset[0]] + weightmatrix[currset[0]][prevval]
        minval = currset[0]
        for i in currset:
            if(minimum > dp[currsetstart,i] + weightmatrix[i][prevval]):
                minimum = dp[currsetstart,i] + weightmatrix[i][prevval]
                minval = i
        path.append(minval)
        currsetstart = copy.deepcopy(list(currsetstart))
        currsetstart.remove(minval)
        currsetstart = tuple(currsetstart)
        currset.remove(minval)
        prevval = minval
    
    path.append(currset[0])
    path.append(start)
    path.reverse()
    return path
   

